package Mentoring;

public class Task1 {

    public static void main(String[] args) {
        byte aByteNumber = 99;
        System.out.println(aByteNumber);

        short aShortNumber = 30000;
        System.out.println(aShortNumber);

        int anIntNumber = 1000000;
        System.out.println(anIntNumber);

        long aLongNumber = 8435462265432551L;
        System.out.println(aLongNumber);

        System.out.println(aByteNumber + "\n" + aShortNumber); // without \n it'd sum these two numbers

        float first = 9.75369845236541892348624879F; // it will return 6 decimals
        double second = 9.75369845236541892348624879; // this will return 15 decimals
        System.out.println(first + "\n" + second);

        char backSlash = '\\';
        System.out.println(backSlash);



    }
}
