public enum Days {
    // we store constant values and separate them with comma
    /**
     * To store multiple constance variables we use ENUMS
     */
    // all these values are final
            // we can store numbers in enums
            // we store something constant in enums, like months/days and stuff that cannot be changed
            // these constants should represent something, to make sense
    MONDAY,
    TUESDAY,
    WEDNESDAY,
    THURSDAY,
    FRIDAY,
    SATURDAY,
    SUNDAY
    // for the last one we don't put anything (no comma)


}
