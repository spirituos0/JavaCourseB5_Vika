package _01_JAVA.N_Loops._02_WhileLoop;

public class Task2_PrintNumbers {
    public static void main(String[] args) {

        for (int i = 0; i <=10; i++) {
            System.out.println(i);
        }

        int i = 0;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }

}
