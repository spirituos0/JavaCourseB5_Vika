package _01_JAVA.N_Loops._01_ForLoop;

public class T_ForLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 20; i++) {
            System.out.println("Let's loop it " + i);
        }

        for (int i = 20; i > 0; i--) {
            System.out.println("Let's loop it reverse " + i);
        }
    }
}
