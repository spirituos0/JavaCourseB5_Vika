package _01_JAVA.B_PrimitiveTypes.Homeworks;

public class Byte {

    public static void main(String[] args) {

        byte cKal = 5; // in one "Extra" gum
        System.out.println(cKal);

        byte VATemp = -4; // the lowest temperature in Virginia in winter last year
        System.out.println(VATemp);

        byte years = 127; // from the day Nikita Khrushchev was born (Soviet Premier)
        System.out.println(years);

        byte iWillDieIfILoseThisMuchWeight = -128; // in lbs
        System.out.println(iWillDieIfILoseThisMuchWeight);

    }
}
