package _01_JAVA.B_PrimitiveTypes.Homeworks;

public class Char {

    public static void main(String[] args) {

        char lastLetterOfMyFirstName = 'a';
        char firstLetterOfMyLastName = 'A';
        char and = '&';
        char assignmentsLeftToDo = '1';
        char singleQuote = '\'';
        char backslash = '\\';

        System.out.println(lastLetterOfMyFirstName);
        System.out.println(firstLetterOfMyLastName);
        System.out.println("Symbol for \"and\": " + and);
        System.out.println("Assignments left to do: " + assignmentsLeftToDo);
        System.out.println("Single quote symbol: " + singleQuote);
        System.out.println("Backslash symbol: " + backslash);

    }
}
