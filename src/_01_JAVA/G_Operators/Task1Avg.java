package _01_JAVA.G_Operators;

public class Task1Avg {
    public static void main(String[] args) {
        double num1 = 23.4;
        double num2 = 24.0;
        double num3 = 12;
        double num4 = 450.3;
        double num5 = 23000;

        // find the average for these doubles

        double sum = num1 + num2 + num3 + num4 + num5;
        System.out.println(sum); // 23509.7

        double average = 23509.7;
        average /= 5;
        System.out.println("Average: " + average);




    }

}


