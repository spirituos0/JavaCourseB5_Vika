package _01_JAVA.H_TypeCasting;

public class Task1 {
    public static void main(String[] args) {

        short shortNr = 350;
        long longNr = shortNr;
        System.out.println(longNr);

        float floatNr = 4.3421F;
        double doubleNr = floatNr;
        System.out.println(doubleNr);

        byte byteNr = 121;
        float floatNr2 = byteNr;
        System.out.println(floatNr2);
    }
}
