package _01_JAVA.W_Exceptions;

public class _06_TryCatchFinally {

    public static void main(String[] args) {

        try {
            // try to do this
        } catch (Exception ex) {
            // if there is an exception, do this
        } finally {
            // do this anyway
        }
        /**
             Finally
                - is used to place important code,
                  it will be executed whether exception is handled or not;
                - is a block
         */
    }
}
