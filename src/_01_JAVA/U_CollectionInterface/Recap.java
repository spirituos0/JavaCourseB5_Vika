package _01_JAVA.U_CollectionInterface;

public class Recap {

    /**
            ARRAY VS ARRAYLIST
      - Array is fixed-size, Arraylist is resizable
      - Array can store both primitive and reference type, ArrayList can only store reference type
      - length() method used to calculate size for Array, size() method used for ArrayList

     * Both Array and ArrayList can contain duplicate values

           ARRAYLIST VS SET
     - Set is unordered, ArrayList uses insertion order
     - Set doesn't allow duplicate values, ArrayList allows duplicate values

           HashSet
     - Unordered Set - doesn't allow duplicate values

           LinkedHashSet
     - It is using insertion order

           TreeSet
     - It is unordered Set (using natural order - Ascending order)

     *** We cannot use get() method with Sets

     */

}
