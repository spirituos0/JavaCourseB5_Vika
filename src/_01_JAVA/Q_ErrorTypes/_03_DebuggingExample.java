package _01_JAVA.Q_ErrorTypes;

public class _03_DebuggingExample {

    public static void main(String[] args) {

        for (int j = 0; j < 4; j++) {

            for (int i = 0; i <= j; i++) {
                System.out.println("*");
            }
            System.out.println();
        }
    }
}
