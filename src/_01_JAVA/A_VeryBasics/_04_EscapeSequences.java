package _01_JAVA.A_VeryBasics;

public class _04_EscapeSequences {

    public static void main(String[] args) {

        // I learn "Java" in TechnoStudy
        // Backslash \ helps to escape from " sequence and prevent Java from confusing
        System.out.println("I learn \"Java\" in TechnoStudy");

        System.out.println("C:\\ProgramFiles\\...");
        // One backslash is for escaping, second one will be printed

        // java is a very good
        //Programming language
        System.out.println("Java is very good\nprogramming language!");
        // To move to a new line - \n
        // It prevents making is complicated with the separate print method

        System.out.println("TechnoStudy");
        System.out.println("\tTechnoStudy");
        // To use tab - \t
        // It puts tab in the beginning (3 extra spaces)

    }

}
