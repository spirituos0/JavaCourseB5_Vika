package _01_JAVA.A_VeryBasics;

public class _03_Print {
    public static void main(String[] args) {
        System.out.print("Welcome");
        System.out.print("to");
        System.out.print("Java");
        System.out.print("Class");

        System.out.println(" ");
        /* Print method:
        Only after System.out.println(" "); next value will move to the next line.
        Space stays on the top value. Then word moves down.
        But instead of separating space in 'println' I can just put 'println' for the last word 'Class'
        Example: System.out.println("Class");
        Second way is correct and fast => Better
         */

        System.out.print("1");
        System.out.print("2");
        System.out.print("3");
        System.out.print("4");
        System.out.print("5");

    }
}
